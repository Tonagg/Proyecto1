package src;

/**
 * Enumeración de las sucursales disponibles de MonosChinos MX.
 * Cada valor representa una ubicación geográfica donde se procesan
 * y registran las compras.
 */
public enum Sucursal {

    /** Sucursal en Ciudad de México. */
    CDMX,

    /** Sucursal en Chihuahua. */
    CHIHUAHUA,

    /** Sucursal en Jalisco. */
    JALISCO,

    /** Sucursal en Yucatán. */
    YUCATAN
}